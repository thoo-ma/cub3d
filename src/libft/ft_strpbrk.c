#include "libft.h"

char	*ft_strpbrk(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	while (*s1)
	{
		if (ft_strchr(s2, *s1))
			return (s1);
		s1++;
	}
	return (0);
}
